              <h5 class="card-title">Murales decorativos para exterior</h5>
              <p class="card-text texto_public">Los graffitis murales decorativos para exterior son las obras que se realizan al aire libre. Abraza desde el gran formato a pequeñas superficies que normalmente serán en vertical, es decir, trabajar sobre una pared adecuándose al paisaje urbano. <br> Ten en cuenta que, un mural decorativo exterior, cuanto más grande sea, los costes de ejecución aumentarán por la necesidad de utilizar herramientas incomodas para el trabajo o la maquinaria elevadora.</p>
