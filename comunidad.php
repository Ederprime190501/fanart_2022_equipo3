              <h5 class="card-title">Murales Geometricos</h5>
              <p class="card-text texto_public">Si eres un amante de las matemáticas, los artistas también tienen la habilidad de poder “jugar” con un muro y aplicar las mismas reglas espaciales que se usarían en el plano.Utilizan figuras poligonales de distinto tipo; incluyendo triángulos, cuadrados, rombos…¡todo tipo de geometrías! Si bien es cierto que muchos profesionales tienen la capacidad de elaborar líneas rectas a mano alzada, sin ningún tipo de ayuda extra, otros tantos artistas utilizan cinta de carrocero para hacer formas perfectas.</p>
